local px, py = 22,12 -- position de la camera
local dx, dy = -1,0 -- direction de la camera
local planeX,planeY = 0,0.66 -- ratio de la camera
local time, oldTime = 0,0 -- timer
local w = love.graphics.getWidth() -- largeur de l'écran
local h = love.graphics.getHeight() -- hauteur de l'écran
local deltaMove, deltaRotation = 0,0 -- deplacement et rotation
local spdMove, spdRotation = 5,3 -- vitesse de deplacement et rotation

-- keyboard
local toV,toH = 0,0 -- direction de deplacement recuperer en fonction des touches du clavier appuyé
local toUp, toLeft, toDown, toRight = "up", "left", "down", "right"
local toUpD, toLeftD, toDownD, toRightD = 1, -1, -1, 1

