-- Gestion du clavier
function love.keypressed(key)
	if key == "escape" then -- pour quitter (mieux qu'un bouton quitter mageul !)
		love.event.quit()
	end

	if key == toUp then
		toV = toV+toUpD
	end
	if key == toDown then
		toV = toV+toDownD
	end
	if key == toLeft then
		toH = toH+toLeftD
	end
	if key == toRight then
		toH = toH+toRightD
	end   
   
   
end

function love.keyreleased(key)
	if key == toUp then
		toV = toV-toUpD
	end
	if key == toDown then
		toV = toV-toDownD
	end
	if key == toLeft then
		toH = toH-toLeftD
	end
	if key == toRight then
		toH = toH-toRightD
	end 
end










